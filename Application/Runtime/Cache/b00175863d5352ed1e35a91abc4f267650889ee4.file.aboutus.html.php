<h4>（一）江西理工大学记者团简介</h4>
<p>江西理工大学记者团是校党委宣传部领导并提供业务指导的学生社团，正式成立于2009年9月，由一批热爱写作、钟情文字并且具备一定摄影功底的在校大学生组成，其业务范围涉及新闻采写、摄影摄像等多个领域。</p>
<p>记者团成立以来，始终坚持以“打造江西一流、全校领先的育人社团”为立团宗旨，以“以贡献求支持”为工作理念，以“传承校园文化，树立理工形象，传递新闻信息，抒发青春情怀”为团队目标，旨在通过新闻采写编辑、专题策划报道、讯息收集、舆论引导等方式，塑造一批具有良好文字功底、新闻素养和摄影水平的优秀大学生记者，并竭力将团队打造成为学校支持、师生点赞、成员满意的江西省最具传播力的校园学生媒体。</p>
<p>迄今为止，记者团成员已在各大报刊、网站等媒体已取得了丰硕的果实，目前记者团成员先后在新华社、中新社、《人民日报》、《光明日报》、《中国教育报》、《中国体育报》、《中国矿业报》、《科技日报》等国内主流媒体发表稿件上千篇。在人民网、光明网、中国广播网、新浪、网易、搜狐等网络媒体发表稿件万余篇。</p>
<p>除了日常采写工作之外，团队还会开展诸如新闻写作培训、名家讲座、人文知识竞赛等一系列特色活动，在为师生提供一个展示自我、丰富业余生活平台的同时，也为学校校园文化建设贡献力量。<p>
<p>记者团业已成为我校极具特色的育人平台，团队成立以来，先后有十余人次入选广州亚运会、深圳世界大学生运动会、中国红歌会、江西省山水情歌会、大学生走进广东边防海岛部队、走进双汇等国内外知名活动大学生注册记者；4人次获得江西省“校媒之星”，20余人次获得江西省优秀教育新闻奖、江西省高校好新闻奖、江西省“十佳校媒记者”、江西省“优秀学生记者”、深圳世界大学生运动校园传播大使、中国红歌会校园传播大使等荣誉称号。</p>
<p>团队成员连续高票入选中国（江西）高校传媒联盟主席团，部分优秀成员还可以被推荐参加江西省乃至全国各大校媒活动，与清华大学、武汉大学、台湾大学、中国传媒大学等著名高校传媒精英共同交流、共同成长。此外，党委宣传部还会定期选派部分成员前往信息日报、江南都市报、赣州晚报等媒体单位实习，竭力提升学生业务能力。因为业绩突出，团队先后有多名学生记者被江南都市报、赣州晚报、策划营销公司等单位录用，获得了良好的就业机会。</p>
<p>记者团是一个温馨、有爱的集体，有独立开放的工作室，成员内部除了常规工作交流外，还会不定期举行集体烧烤、素质拓展、集体踏青、集体学习等内部交流活动，节日时节还会互赠贺卡。</p>
<p>2011年3月，记者团成功荣膺“江西省高校‘最具社会传播力媒体’”荣誉称号；2012年5月荣获“江西省十佳校园媒体”称号。期间，我校党委书记叶仁荪还寄语团队：“传承校园文化，树立理工形象，传递新闻信息，抒发青春情怀”，语文出版社社长、教育部原新闻发言人王旭明老师还欣然为记者团成员题词：“做有思想的大学生”。</p>
