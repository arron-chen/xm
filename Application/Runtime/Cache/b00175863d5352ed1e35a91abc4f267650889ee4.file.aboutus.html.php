<p>杂志社主打文学特色，积极承办校园“三行情书”大赛等文学活动，活跃理工校园创作氛围。杂志社还同《江西理工大学报》合作开展每年的新生征文活动，以发掘新生代写作人才，构筑理工自家文化圈。在文学交流方面，杂志社与清江文学社合办“清江杯”诗词大赛，通过诗词填空、诗词接龙等趣味游戏引导理工学子重拾诗词文化的魅力。作为公益性学生团体，清江杂志社助力慈善活动，借助杂志义卖、爱心募捐等形式为我校重病学生募款、向乡村儿童捐书，实现文学与公益的良性结合。</p>

