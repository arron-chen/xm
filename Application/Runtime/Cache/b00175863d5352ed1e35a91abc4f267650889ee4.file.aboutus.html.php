<p>采编规范及用稿方针：在确保学校每日新闻必须播报的基础上，采用其他一些二级学院重要的新闻稿件，这些稿件合格与否须经本节目负责人和专项负责的老师进行审核。综艺彩虹的文学类稿件接受全校师生投稿，有专人审核。而对于英语音乐、人物访谈等用稿则为专人撰稿。</p>
